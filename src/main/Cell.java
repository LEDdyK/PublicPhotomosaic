package Photomosaic;

public class Cell {
	private int R;
	private int G;
	private int B;
	
	public int getR() {
		return R;
	}
	public int getG() {
		return G;
	}
	public int getB() {
		return B;
	}
	
	public void setR(int R) {
		this.R = R;
	}
	public void setG(int G) {
		this.G = G;
	}
	public void setB(int B) {
		this.B = B;
	}
}
